        $curl = curl_init($url);
            if ( $curl ) {
            curl_setopt($curl, CURLOPT_RETURNTRANSFER, true);
            $response = curl_exec($curl);
            curl_close($curl);
            unset($curl);
            $decode=json_decode($response);
            foreach ($decode->rates as $name => $rate){
                Currency::updateOrCreate([
                "name"=>$name,
                ],
                [
                "name"=>$name,
                "exchange"=>$rate,
                ]);
            }
        }

    }

